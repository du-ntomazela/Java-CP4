CREATE TABLE ESTOQUE (
    ID             NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TIPO           VARCHAR2(20)    NOT NULL,
    NOME           VARCHAR2(100)   NOT NULL,
    QTD            NUMBER          NOT NULL,
    CAPACIDADE     NUMBER          NOT NULL,
    LIMPO          CHAR(1),
    MATERIAL       VARCHAR2(50),
    VALIDADE_DIAS  NUMBER
);
